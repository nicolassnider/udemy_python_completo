'''
Problema 04: Hallar la potencia de a^n, donde a y n pertenecen a
números enteros positivos.
Análisis: Para la solución de este problema, se requiere que
usuario ingrese el valor de a y n por teclado y el sistema
realice el cálculo respectivo y obtenga la potencia p.
'''

valor=int(input("valor:\n"))
exponente=int(input("exponente:\n"))

portencia=valor ** exponente

print(f"portencia = {portencia}")
